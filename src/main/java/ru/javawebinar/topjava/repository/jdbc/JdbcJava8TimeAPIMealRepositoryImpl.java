package ru.javawebinar.topjava.repository.jdbc;

import org.springframework.context.annotation.Profile;
import org.springframework.jdbc.core.JdbcTemplate;
import org.springframework.jdbc.core.namedparam.NamedParameterJdbcTemplate;
import org.springframework.stereotype.Repository;

import java.time.LocalDateTime;

@Repository
@Profile("postgres")
public class JdbcJava8TimeAPIMealRepositoryImpl extends AbstractJdbcMealReposiroty<LocalDateTime> {


    public JdbcJava8TimeAPIMealRepositoryImpl(JdbcTemplate jdbcTemplate, NamedParameterJdbcTemplate namedParameterJdbcTemplate) {
        super(jdbcTemplate, namedParameterJdbcTemplate);
    }

    @Override
    protected LocalDateTime convertDateTime(LocalDateTime dateTime) {
        return dateTime;
    }
}
